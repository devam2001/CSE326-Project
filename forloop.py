#for loop in python
print("loop 1")
for x in range(5):print(x,"Hello!")
print("loop 2")
for y in range(1,5):print(y)            #range(start_Val,upto)
print("loop 3")
for z in range(1,10,2):print(z)         #range(LOWER BOUND,UPPER BOUND,increment)
print("loop 4")
for y in range(10,1,-2):print(y)        #range(UPPER BOUND,LOWER BOUND,decrement)
print("loop 5")
for y in range(5):print(y,end=" ")      #for printing output in one line
